#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	len1;
	size_t	len2;
	char	*ptr;

	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	ptr = malloc(len1 + len2 + 1);
	if (!ptr)
		return (0);
	ft_strlcpy(ptr, s1, len1 + len2 + 1);
	ft_strlcat(ptr, s2, len1 + len2 + 1);
	return (ptr);
}
